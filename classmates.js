function getClassmateInteractionCost(def, interactionType) {
  return -((def?.relationshipStoryPools?.[interactionType]?.low?.[0]?.effect?.money) || 0)
}

function pickClassmateInteractionStory(def, rel, interactionType) {
  if (!def || !interactionType) return null

  const interaction = def.interactions?.[interactionType]
  const pools = def.relationshipStoryPools?.[interactionType]
  if (!interaction || !pools) return null

  let candidates = null
  if (rel?.lover) candidates = pools.lover
  else if ((rel?.affinity ?? 0) <= 50) candidates = pools.low
  else candidates = pools.high

  if (!Array.isArray(candidates) || !candidates.length) return null
  return candidates[Math.floor(Math.random() * candidates.length)]
}
